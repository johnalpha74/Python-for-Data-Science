# Python program to demonstrate
# string slicing using List/Arrays

# String slicing using List/Arrays
string_to_slice = 'MYNEWSTRING'

# slicing using indexing sequence (stop-1 .i.e 3-1)
print(string_to_slice[:3])

# String slicing
string_to_slice = 'MYNEWSTRING'

# Using indexing sequence
print(string_to_slice[1:8:2])

# Using indexing sequence in reverse
print(string_to_slice[-1:-10:-2])

# Printing the whole string in reverse order
print(string_to_slice[::-1])

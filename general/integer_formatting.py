# Formatting of Integers using the Python format() method

# Formatting of integers
my_string = "{0:b}".format(23)
print("\nBinary representation of 23 is ")
print(my_string)

# Formatting of Floats
my_string = "{0:e}".format(256.768)
print("\nExponent representation of 256.768 is ")
print(my_string)

# Rounding off Integers
my_string = "{0:.2f}".format(2/5)
print("\ntwo-fifth is : ")
print(my_string)


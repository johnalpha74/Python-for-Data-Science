# Accessing lists in Python
# Using negative index

my_list = ['Code', 'Brewers', 'Boot', 'Camp']

# print the last element of list
print("\nThe last list is accessed using my_list[-1]" + " and is: " + str(my_list[-1]))

# print the third last element of list
print("\nThe third last element of the is accessed using (my_list[-3]): " + str(my_list[-3]))

# Getting the length of a Python list
list_length = len(my_list)

print('\nThe length of the list is: ' + str(list_length))

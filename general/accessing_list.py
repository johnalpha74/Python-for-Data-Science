# Accessing a Python list using the index operator

my_list = ['Code', 'Brewers', 'Boot', 'Camp']
length_of_list = len(my_list)
print("The first element in the list is: " + "\n" + str(my_list[0]))
print("\nThe 3rd element in the list is: " + "\n" + str(my_list[2]))
print("\nThe last element in the last is: " + "\n" + str(my_list[length_of_list-1]))


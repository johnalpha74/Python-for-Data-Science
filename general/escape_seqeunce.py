# Escape Sequencing of String in Python

# Escaping with use of Triple Quotes
my_string = '''I'm called "John Alpha"'''
print("Example number 1 of escaping sequence using Triple Quotes: ")
print(my_string)

# Escaping with a Single Quote
my_string = 'I\'m called "John Alpha"'
print("\nExample of escaping using single quotes")
print(my_string)

# Escaping using Double Quotes
my_string = "I'm called \"John Alpha\""
print("\nExample of escaping using Double Quotes")
print(my_string)

# Escaping for paths of a directory
my_string = "C:\\MyUniversity\\Python\\Lessons"
print("\nPrinting paths using escape sequence")
print(my_string)

#Escaping using Tab
my_string = "\nThis is an example of a \tTab escaping"
print(my_string)

# Using of New Line
my_string = "John\nAlpha"
print("\nNew Line Escaping Example: ")
print(my_string)



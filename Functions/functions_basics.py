# A function is a reusable piece of code
# It can take one or more inputs
# A function ca return an output

# Define a function using the def keyword
def greetings():
    print("Hello my friend")
    print("Please enjoy your day")


greetings()

# Functions can also accept arguments
# What is the difference between a parameter and an argument


def greetings(name):
    print("Hello {}".format(name))


greetings("John")
greetings("Princess")




